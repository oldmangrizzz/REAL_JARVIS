// MARK: - ARCSubmissionOrchestratorError

enum ARCSubmissionOrchestratorError: Error, LocalizedError {
    case unableToReadTaskFile
    case invalidTaskJSON(Error)
    case physicsProcessingFailed(Error)
    case proposalGenerationFailed(Error)
    case invalidOutputShape
    
    var errorDescription: String? {
        switch self {
        case .unableToReadTaskFile:
            return "Unable to read the ARC task file."
        case .invalidTaskJSON(let underlying):
            return "Failed to decode ARC task JSON: \(underlying.localizedDescription)"
        case .physicsProcessingFailed(let underlying):
            return "Physics bridge failed: \(underlying.localizedDescription)"
        case .proposalGenerationFailed(let underlying):
            return "Python RLM bridge failed: \(underlying.localizedDescription)"
        case .invalidOutputShape:
            return "The generated proposal does not match the expected output shape."
        }
    }
}

// MARK: - ARCSubmissionOrchestrator

/// Orchestrates the end‑to‑end ARC‑AGI submission flow:
/// 1. Loads a task definition from a JSON file.
/// 2. Runs the physics bridge to obtain a physics‑augmented representation.
/// 3. Calls the Python RLM bridge to generate a proposal.
/// 4. Validates the shape of the proposal.
/// 5. Packages everything into an ``ARCSubmissionArtifact``.
struct ARCSubmissionOrchestrator {
    
    // MARK: Shared Instance
    
    static let shared = ARCSubmissionOrchestrator()
    
    // MARK: Public API
    
    /// Executes the full submission pipeline for a given ARC task file.
    ///
    /// - Parameter taskFileURL: The file URL pointing to a JSON file that conforms to the ARC task schema.
    /// - Returns: An ``ARCSubmissionArtifact`` containing the original task and the generated proposal.
    /// - Throws: ``ARCSubmissionOrchestratorError`` if any step of the pipeline fails.
    func run(taskFileURL: URL) async throws -> ARCSubmissionArtifact {
        // 1️⃣ Load and decode the task JSON.
        let taskData: Data
        do {
            taskData = try Data(contentsOf: taskFileURL)
        } catch {
            throw ARCSubmissionOrchestratorError.unableToReadTaskFile
        }
        
        let task: ARCSubmissionTask
        do {
            let decoder = JSONDecoder()
            task = try decoder.decode(ARCSubmissionTask.self, from: taskData)
        } catch {
            throw ARCSubmissionOrchestratorError.invalidTaskJSON(error)
        }
        
        // 2️⃣ Run the physics bridge.
        let physicsResult: ARCPhysicsResult
        do {
            physicsResult = try ARCPhysicsBridge.shared.process(task: task)
        } catch {
            throw ARCSubmissionOrchestratorError.physicsProcessingFailed(error)
        }
        
        // 3️⃣ Generate a proposal via the Python RLM bridge.
        let proposal: ARCProposal
        do {
            proposal = try await PythonRLMBridge.shared.propose(using: physicsResult)
        } catch {
            throw ARCSubmissionOrchestratorError.proposalGenerationFailed(error)
        }
        
        // 4️⃣ Validate the shape of the proposal.
        guard proposal.isValidShape(for: task) else {
            throw ARCSubmissionOrchestratorError.invalidOutputShape
        }
        
        // 5️⃣ Package everything into an artifact.
        let artifact = ARCSubmissionArtifact(task: task, proposal: proposal, physicsResult: physicsResult)
        return artifact
    }
}

