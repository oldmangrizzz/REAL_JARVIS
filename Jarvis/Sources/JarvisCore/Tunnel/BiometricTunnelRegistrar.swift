/// Handles registration of a tunnel endpoint using biometric authentication.
///
/// The registrar creates (or retrieves) a Secure Enclave private key that is bound to the
/// device owner authentication context. It then signs the concatenation of the device identifier
/// and a sealed proof supplied by the caller. The resulting signature can be sent to the
/// remote side to prove possession of the device and the biometric‑protected key.
public final class BiometricTunnelRegistrar {
    
    // MARK: - Public API
    
    /// Register a phone‑side tunnel endpoint.
    ///
    /// - Parameters:
    ///   - deviceID: The identifier of the device (e.g. UUID bytes).
    ///   - sealedProof: A proof generated by the remote side that must be included in the signature.
    /// - Returns: A DER‑encoded signature that can be verified with the public key stored in the tunnel.
    /// - Throws: Propagates any LAContext or CryptoKit errors.
    public func registerPhone(deviceID: Data, sealedProof: Data) throws -> Data {
        return try register(deviceID: deviceID, sealedProof: sealedProof, policy: .deviceOwnerAuthenticationWithBiometrics)
    }
    
    /// Register a watch‑side tunnel endpoint.
    ///
    /// The watch does not have a biometric sensor, therefore the registration uses the generic
    /// `deviceOwnerAuthentication` policy (PIN / passcode fallback is allowed).
    ///
    /// - Parameters:
    ///   - deviceID: The identifier of the watch.
    ///   - sealedProof: A proof generated by the remote side that must be included in the signature.
    /// - Returns: A DER‑encoded signature that can be verified with the public key stored in the tunnel.
    /// - Throws: Propagates any LAContext or CryptoKit errors.
    public func registerWatch(deviceID: Data, sealedProof: Data) throws -> Data {
        return try register(deviceID: deviceID, sealedProof: sealedProof, policy: .deviceOwnerAuthentication)
    }
    
    // MARK: - Private Helpers
    
    /// Core registration implementation shared by phone and watch.
    private func register(deviceID: Data, sealedProof: Data, policy: LAPolicy) throws -> Data {
        // 1️⃣ Create an LAContext that will be used for both policy evaluation and as the
        //    authentication context for the Secure Enclave key.
        let context = LAContext()
        var authError: NSError?
        let reason = "Authenticate to register this device with the tunnel."
        
        // Verify that the requested policy can be evaluated.
        guard context.canEvaluatePolicy(policy, error: &authError) else {
            throw authError ?? NSError(domain: "BiometricTunnelRegistrar",
                                       code: -1,
                                       userInfo: [NSLocalizedDescriptionKey: "Unable to evaluate authentication policy."])
        }
        
        // 2️⃣ Synchronously evaluate the policy.  The API is asynchronous, so we bridge it
        //    with a semaphore to keep the public method synchronous.
        var evaluationSucceeded = false
        var evaluationError: Error?
        let semaphore = DispatchSemaphore(value: 0)
        context.evaluatePolicy(policy, localizedReason: reason) { success, error in
            evaluationSucceeded = success
            evaluationError = error
            semaphore.signal()
        }
        semaphore.wait()
        
        guard evaluationSucceeded else {
            throw evaluationError ?? NSError(domain: "BiometricTunnelRegistrar",
                                            code: -2,
                                            userInfo: [NSLocalizedDescriptionKey: "User authentication failed."])
        }
        
        // 3️⃣ Obtain (or create) a Secure Enclave private key that is bound to the same LAContext.
        //    The key is stored in the keychain so that subsequent calls reuse it.
        let keyTag = "com.example.tunnel.biometrickey".data(using: .utf8)!
        let accessControl = SecAccessControlCreateWithFlags(nil,
                                                            kSecAttrAccessibleWhenUnlockedThisDeviceOnly,
                                                            .privateKeyUsage,
                                                            nil)!
        
        // Try to fetch an existing key.
        var query: [String: Any] = [
            kSecClass as String: kSecClassKey,
            kSecAttrApplicationTag as String: keyTag,
            kSecAttrKeyType as String: kSecAttrKeyTypeECSECPrimeRandom,
            kSecReturnRef as String: true,
            kSecUseAuthenticationContext as String: context
        ]
        
        var item: CFTypeRef?
        let status = SecItemCopyMatching(query as CFDictionary, &item)
        let privateKey: SecureEnclave.P256.Signing.PrivateKey
        
        if status == errSecSuccess, let secKey = item as! SecKey? {
            // Wrap the existing SecKey in a CryptoKit key.
            privateKey = try SecureEnclave.P256.Signing.PrivateKey(secKey: secKey)
        } else {
            // No existing key – create a new one.
            privateKey = try SecureEnclave.P256.Signing.PrivateKey(accessControl: accessControl,
                                                                   authenticationContext: context)
            
            // Store the newly‑created key in the keychain.
            let addQuery: [String: Any] = [
                kSecClass as String: kSecClassKey,
                kSecAttrApplicationTag as String: keyTag,
                kSecValueRef as String: privateKey.secKey,
                kSecAttrAccessible as String: kSecAttrAccessibleWhenUnlockedThisDeviceOnly,
                kSecAttrAccessControl as String: accessControl,
                kSecUseAuthenticationContext as String: context
            ]
            let addStatus = SecItemAdd(addQuery as CFDictionary, nil)
            guard addStatus == errSecSuccess else {
                throw NSError(domain: "BiometricTunnelRegistrar",
                              code: Int(addStatus),
                              userInfo: [NSLocalizedDescriptionKey: "Failed to store Secure Enclave key in keychain."])
            }
        }
        
        // 4️⃣ Build the data to be signed: deviceID || sealedProof.
        var payload = Data()
        payload.append(deviceID)
        payload.append(sealedProof)
        
        // 5️⃣ Sign the payload using the Secure Enclave key.
        let signature = try privateKey.signature(for: payload)
        
        // Return the DER‑encoded signature (the remote side knows how to verify it).
        return signature.derRepresentation
    }
